# Зчитуємо вхідний рядок
s = input()

# Зчитуємо межі підрядка (позиції з 1-індексацією)
l, r = map(int, input().split())

# Отримуємо підрядок із позиції l до r включно
substr = s[l - 1:r]

# Обчислюємо довжину підрядка
length = len(substr)

# Виводимо загальну кількість префіксів:
# включає порожній префікс та повний підрядок
print(length + 1)

# Виводимо всі власні префікси (тобто ті, що коротші за увесь рядок)
for i in range(1, length):
    print(substr[:i])
