# Зчитуємо зразок P та текст T
P = input()  # Рядок-зразок (pattern), який потрібно знайти
T = input()  # Рядок-текст, у якому виконується пошук

# Список для збереження позицій входжень зразка в текст
positions = []

# Лічильник кількості порівнянь символів
comparisons = 0

# Проходимо по всіх можливих позиціях у тексті, з яких може починатися зразок
# len(T) - len(P) + 1 — кількість таких можливих позицій
for i in range(len(T) - len(P) + 1):
    match = True  # Позначаємо, що поточне порівняння поки що вдале
    # Порівнюємо символи зразка та тексту справа наліво
    for j in range(len(P) - 1, -1, -1):
        comparisons += 1  # Збільшуємо лічильник порівнянь
        if T[i + j] != P[j]:  # Якщо символи не співпадають
            match = False     # Встановлюємо, що збіг не знайдено
            break             # Припиняємо внутрішній цикл
    # Якщо всі символи співпали — додаємо позицію (i + 1) до списку
    if match:
        positions.append(i + 1)  # Індексація з 1

# Виводимо результат
if positions:
    print(*positions)  # Позиції входжень через пробіл
else:
    print(0)  # Якщо входжень немає — виводимо 0
print(comparisons)  # Загальна кількість порівнянь символів
