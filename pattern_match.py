# Зчитуємо зразок (рядок P), текст (рядок T) та позицію k
P = input()         # Зразок (pattern), який потрібно знайти
T = input()         # Текст, у якому здійснюється пошук
k = int(input()) - 1  # Індекс, з якого починаємо пошук (перехід до 0-індексації)

cnt = 0     # Лічильник кількості порівнянь символів
ok = True   # Прапорець, що означає: зразок збігається з текстом з позиції k

# Посимвольне порівняння зразка P та підрядка тексту T, що починається з позиції k
for i in range(len(P)):
    cnt += 1
    # Якщо вийшли за межі рядка T або символи не збігаються — зразок не знайдено
    if k + i >= len(T) or P[i] != T[k + i]:
        ok = False
        break

# Виводимо результат:
# YES — якщо зразок входить у текст з позиції k+1, інакше NO
print("YES" if ok else "NO")
# Виводимо кількість виконаних порівнянь символів
print(cnt)
