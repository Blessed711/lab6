# Зчитуємо рядок
s = input()
n = len(s)  # Довжина рядка

# Створюємо множину для зберігання всіх різних підрядків
substrings = set()

# Перебираємо всі можливі підрядки
for i in range(n):  # Початок підрядка
    for j in range(i + 1, n + 1):  # Кінець підрядка (невключно)
        substrings.add(s[i:j])  # Додаємо підрядок до множини

# Виводимо кількість різних (унікальних) непорожніх підрядків
print(len(substrings))
